package vadim_nedrega.HW17_XML.XMLDataStore;

public class XMLDataStoreException extends Exception {
    public XMLDataStoreException() {
        super();
    }

    public XMLDataStoreException(String message) {
        super(message);
    }

    public XMLDataStoreException(String message, Throwable cause) {
        super(message, cause);
    }

    public XMLDataStoreException(Throwable cause) {
        super(cause);
    }
}
